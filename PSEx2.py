# べき乗の優先順位====================

# べき乗は右から計算されていく

### ① 1の3乗
### ② 2の1乗
print(2 ** 1 ** 3)

### べき乗以外は通常通り、左から計算
### ((48/6の整数部) / 4)の整数部
print(48 // 6 // 4)

